package loopsAndSwitch;

public class Creative {
    public static void main(String[] args) {

        for(int counter = 1;  counter == 1; counter = counter + counter){
            System.out.println(counter);
        }

        for(int counter = 222;  counter == 222; counter = counter + counter){
            System.out.println(counter);
        }

        for(int counter = 33333;  counter == 33333; counter = counter + counter){
            System.out.println(counter);
        }

        for(int counter = 4444444;  counter == 4444444; counter = counter + counter){
            System.out.println(counter);
        }

        for(int counter = 555555555;  counter == 555555555; counter = counter + counter){
            System.out.println(counter);
        }
    }
}